    private void pauseOrResumeSimulation(){
        boolean status = m_simulation.isPaused();
        if (!status){
            m_simulation.pause();
            m_buttonPause.setText("Resume");
        } else {
            m_simulation.resume();
            m_buttonPause.setText("Pause");
        }
    }
    
