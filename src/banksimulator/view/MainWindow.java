    private void settingCombosBoxes(){
        m_agentCombo01.setSelectedIndex(-1);
        m_agentCombo02.setSelectedIndex(-1);
        
        DefaultComboBoxModel<String> model = (DefaultComboBoxModel<String>) m_agentCombo01.getModel();
        DefaultComboBoxModel<String> model2 = (DefaultComboBoxModel<String>) m_agentCombo02.getModel();
        
        model.removeAllElements();
        model2.removeAllElements();
        
        model.addElement("EXTRATO de um agente");
        model2.addElement("EXTRATO de um agente");
        
        SimulatorAgent[] agents = m_simulation.getAgents();
        for(int i=0; i < agents.length; i++){
            model.addElement( agents[i].getName());
            model2.addElement(agents[i].getName());
        }
        //model.setSelectedItem(null);
        //model2.setSelectedItem(null);
        m_agentCombo01.setModel(model);
        m_agentCombo01.setSelectedIndex(0);
        
        //m_agentCombo02.setModel(model2);
        m_agentCombo02.setSelectedIndex(0);
        
       
        //m_agentCombo01.updateUI();
    }
    
    private void updateLabels(){
        m_labelThreads.setText(THREADS + m_simulation.getSettings().m_poolSize);
        m_labelOperationNumber.setText(OPERATIONS + 
                m_simulation.getSettings().m_agentConfig.m_operationsNumber);
        m_labelAgents.setText( AGENTS + m_simulation.getSettings().m_agentSize);
    }
