    private void m_agentCombo02ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_m_agentCombo02ActionPerformed
        int selectedIndex = m_agentCombo02.getSelectedIndex();
          if (selectedIndex <= 0)
           return;
       
       Account account =  m_simulation.getAgentAt(selectedIndex-1).getSimulatorClient().getAccount();
       CopyOnWriteArrayList<Log> logs = account.getLogList();
       String text="";
       
       for(Log l: logs){
           text+= l.getReport();
       }
       text+= "\nTOTAL REGISTROS: " + logs.size();
       m_agentArea02.setText("");
       m_agentArea02.setText(text);
    }//GEN-LAST:event_m_agentCombo02ActionPerformed

